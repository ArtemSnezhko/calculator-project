# Простой калькулятор
def add(a, b):
    return a + b
def subtract(a, b):
    return a - b
def multiply(a, b):
    return a * b
def divide(a, b):
    return a / b
# Тестим функции
print("Сложение: ", add(5, 3))# Вывод: 8
print("Вычитание: ", subtract(10, 4)) # Вывод: 6
print("Умножение: ", multiply(2, 3)) # Вывод: 6
print("Деление: ", divide(8, 0)) # Ошибка!